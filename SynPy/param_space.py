def param_space(df_col):
    """
    Given a dataframe column (series) containing protocol label (index) and values (column), construct a parameter space matrix.
    """
    if not isinstance(df_col, pd.Series):
        raise ValueError('Passed object is not a pd.Series.')
        
    heatmap_dict = {}
    for key, value in df_col.to_dict().items():
        params = protocol_params(key)
        heatmap_dict[float(params['bur']), float(params['osc'])] = value
        
    heatmap_df = pd.Series(heatmap_dict).unstack()
    heatmap_df.index.names = ['Pulses / Burst']
    heatmap_df.columns.names = ['Inter-Burst Frequency (Hz)']
    
    return heatmap_df.iloc[:,::-1].T

